public class VariablesDemo {

	public static void main(String[] args) {
		
		int a;            // declaration
		String s;
		
		a=100;            // Assignment
		s="John";
		
		int b=322;        // declaration + Assignment
		
		System.out.println(a);
		System.out.println(s);
		System.out.println(b);
		

	}

}
